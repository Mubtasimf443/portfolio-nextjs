import React from 'react'
import { toast , Bounce, ToastContainer} from "react-toastify";

export function errorToast(msg: string): void {
    toast.error(msg, {
        position: "top-center",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: false,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        transition: Bounce,
    });
    return;
}

